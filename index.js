// // What is a Single Page Application?

// A Single Page Application is a web application or website that interacts with the web browser by 
// dynamically rewriting the current web page with new data from the web server, instead of the default 
// method of the browser loading entire new pages.
// This means that the URL of your website will not change completely (page will not reload), instead it will keep 
// getting content and rewriting the DOM with it instead of loading a new page.
// The goal is faster transitions that make the website feel more like a native app

// //What is REACT?

// A javascript library for building interfaces
// React is a JavaScript library developed by Facebook which, among other things, was used to build Instagram.com. 
// Its aim is to allow developers to easily create fast user interfaces for websites and applications alike. 
// The main concept of React. js is virtual DOM


